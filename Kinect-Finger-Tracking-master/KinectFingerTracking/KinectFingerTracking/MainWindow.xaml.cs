        {
            _handsController.gesture = (((ListBoxItem)this.gesturelistBox.SelectedItem).Content).ToString();

        }

        private void subjectlistBox_SelectionChanged(object sender, SelectionChangedEventArgs e)
        {
            _handsController.subject = (((ListBoxItem)this.subjectlistBox.SelectedItem).Content).ToString();

        }
    }
